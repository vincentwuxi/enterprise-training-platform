#!/usr/bin/env node
/**
 * generate-registry.js
 * ────────────────────
 * Scans all course.manifest.json files under the training root directory
 * and auto-generates:
 *   1. src/courses/registry.generated.js   — manifest imports + lazy component maps
 *   2. vite.aliases.generated.js           — @course-{id} path aliases for Vite
 *
 * Run: node scripts/generate-registry.js
 * Auto-run: npm run dev / npm run build (via predev / prebuild hooks)
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const PLATFORM_DIR = path.resolve(__dirname, '..');
const TRAINING_ROOT = path.resolve(PLATFORM_DIR, '..');

// ── 1. Discover all courses ──
function discoverCourses() {
  const entries = fs.readdirSync(TRAINING_ROOT, { withFileTypes: true });
  const courses = [];

  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    if (entry.name === 'platform' || entry.name === 'node_modules' || entry.name.startsWith('.')) continue;

    const manifestPath = path.join(TRAINING_ROOT, entry.name, 'course.manifest.json');
    if (!fs.existsSync(manifestPath)) continue;

    try {
      const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
      if (!manifest.id) {
        console.warn(`⚠️  Skipping ${entry.name}: manifest missing "id" field`);
        continue;
      }

      // Extract component names from modules or chapters
      const components = extractComponents(manifest);
      if (components.length === 0) {
        console.warn(`⚠️  Skipping ${manifest.id}: no lesson components found`);
        continue;
      }

      courses.push({
        dirName: entry.name,
        courseId: manifest.id,
        components,
      });
    } catch (err) {
      console.warn(`⚠️  Skipping ${entry.name}: ${err.message}`);
    }
  }

  // Sort by courseId for deterministic output
  courses.sort((a, b) => a.courseId.localeCompare(b.courseId));
  return courses;
}

// ── 2. Extract component names from manifest ──
function extractComponents(manifest) {
  const components = new Set();

  // Format A: modules[].component or modules[].lessonKey or modules[].id (if starts with Lesson)
  if (Array.isArray(manifest.modules)) {
    for (const mod of manifest.modules) {
      const comp = mod.component || mod.lessonKey || (mod.id?.startsWith('Lesson') ? mod.id : null);
      if (comp) components.add(comp);
    }
  }

  // Format B: chapters[].lessons[].component or chapters[].lessons[].lessonKey
  if (Array.isArray(manifest.chapters)) {
    for (const chapter of manifest.chapters) {
      if (Array.isArray(chapter.lessons)) {
        for (const lesson of chapter.lessons) {
          const comp = lesson.component || lesson.lessonKey;
          if (comp) components.add(comp);
        }
      }
    }
  }

  return Array.from(components).sort();
}

// ── 3. Convert courseId to a safe JS variable name ──
function toVarName(courseId) {
  // calculus-intuition → calculusIntuition
  return courseId
    .replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase())
    .replace(/^([0-9])/, '_$1');
}

// ── 4. Generate registry.generated.js ──
function generateRegistryFile(courses) {
  const lines = [
    '// AUTO-GENERATED by scripts/generate-registry.js — DO NOT EDIT',
    `// Generated at: ${new Date().toISOString()}`,
    `// Courses: ${courses.length}`,
    '',
    "import { lazy } from 'react';",
    '',
    '// ── Manifest imports ──',
  ];

  // Manifest imports
  for (const c of courses) {
    const varName = toVarName(c.courseId) + 'Manifest';
    lines.push(`import ${varName} from '@course-${c.courseId}/../course.manifest.json';`);
  }

  lines.push('');
  lines.push('// ── Course registry ──');
  lines.push('export const courseRegistry = {');

  // Registry entries
  for (const c of courses) {
    const varName = toVarName(c.courseId) + 'Manifest';
    lines.push(`  '${c.courseId}': {`);
    lines.push(`    manifest: ${varName},`);
    lines.push('    components: {');

    for (const comp of c.components) {
      lines.push(`      ${comp}: lazy(() => import('@course-${c.courseId}/pages/${comp}')),`);
    }

    lines.push('    }');
    lines.push('  },');
  }

  lines.push('};');
  lines.push('');

  return lines.join('\n');
}

// ── 5. Generate vite.aliases.generated.js ──
function generateAliasesFile(courses) {
  const lines = [
    '// AUTO-GENERATED by scripts/generate-registry.js — DO NOT EDIT',
    `// Generated at: ${new Date().toISOString()}`,
    '',
    "import path from 'path';",
    '',
    'export function getCourseAliases(platformDir) {',
    '  const trainingRoot = path.resolve(platformDir, \'..\');',
    '  return {',
  ];

  for (const c of courses) {
    lines.push(`    '@course-${c.courseId}': path.resolve(trainingRoot, '${c.dirName}/src'),`);
  }

  lines.push('  };');
  lines.push('}');
  lines.push('');

  return lines.join('\n');
}

// ── Main ──
function main() {
  console.log('🔍 Scanning courses...');
  const courses = discoverCourses();
  console.log(`📦 Found ${courses.length} courses`);

  // Write registry
  const registryContent = generateRegistryFile(courses);
  const registryPath = path.join(PLATFORM_DIR, 'src/courses/registry.generated.js');
  fs.writeFileSync(registryPath, registryContent, 'utf8');
  console.log(`✅ Generated ${registryPath}`);

  // Write aliases
  const aliasesContent = generateAliasesFile(courses);
  const aliasesPath = path.join(PLATFORM_DIR, 'vite.aliases.generated.js');
  fs.writeFileSync(aliasesPath, aliasesContent, 'utf8');
  console.log(`✅ Generated ${aliasesPath}`);

  // Summary
  console.log('\n📋 Course registry summary:');
  for (const c of courses) {
    console.log(`   ${c.courseId} (${c.dirName}) — ${c.components.length} components`);
  }
  console.log(`\n🎉 Done! ${courses.length} courses registered.`);
}

main();
